package Calculadora;

public class Calculadora {

    /**
     * Description = Esta funcion hace suma de dos valores "num1 y num2" dependiendo del valor boleano
     * @param num1 Recibe un numero entero
     * @param num2 Numero entero
     * @param bool Recibe un valor boolean
     * @return num1 + num2 int
     */
    public static int sumar(int num1, int num2, boolean bool){
        return num1 + num2 ;
    }

    public static int restar(int num1, int num2){
        return num1 - num2 ;
    }
}
